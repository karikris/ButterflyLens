select has_column('public', 'quality_snapshots', 'blind');
select has_column('public', 'quality_snapshots', 'confidence_level');
select has_column('public', 'quality_snapshots', 'interval_method');
select has_column('public', 'quality_snapshots', 'bootstrap_replicates');
