select ok((select relrowsecurity from pg_class where oid = 'public.reviewer_reliability'::regclass), 'reliability remains RLS protected');
select ok(exists(select 1 from pg_policies where schemaname = 'public' and policyname = 'reviewer_reliability_self_read'), 'reviewers retain private self access');
select ok(exists(select 1 from pg_policies where schemaname = 'public' and policyname = 'reviewer_reliability_curator_read'), 'authorized curators retain governance access');
select ok(not has_table_privilege('anon', 'public.reviewer_reliability', 'select'), 'public cannot read private reliability');
select * from finish();
rollback;
