select has_column('public', 'reviewer_reliability', 'snapshot_revision');
select col_is_fk('public', 'reviewer_reliability', 'supersedes_reliability_pk', 'snapshot lineage is a foreign key');
select has_column('public', 'reviewer_reliability', 'sample_count');
select has_column('public', 'reviewer_reliability', 'control_accuracy');
select has_column('public', 'reviewer_reliability', 'sensitivity');
select has_column('public', 'reviewer_reliability', 'specificity');
select has_column('public', 'reviewer_reliability', 'pairwise_agreement');
select has_column('public', 'reviewer_reliability', 'krippendorff_alpha');
select has_column('public', 'reviewer_reliability', 'adjudicated_overlap');
select has_column('public', 'reviewer_reliability', 'interval_level');
select has_column('public', 'reviewer_reliability', 'evidence_fingerprint');
select has_column('public', 'reviewer_reliability', 'blockers');
select has_index('public', 'reviewer_reliability', 'reviewer_reliability_current_domain_idx', 'current domain lookup is indexed');
select has_index('public', 'reviewer_reliability', 'reviewer_reliability_supersedes_pk_idx', 'supersession FK is indexed');
